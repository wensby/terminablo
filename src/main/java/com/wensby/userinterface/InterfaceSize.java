package com.wensby.userinterface;

public class InterfaceSize {

  private final int width;
  private final int height;

  public InterfaceSize(int width, int height) {
    this.width = width;
    this.height = height;
  }

  public int getWidth() {
    return width;
  }

  public int getHeight() {
    return height;
  }
}
